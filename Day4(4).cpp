  bool isSubsequence(string s, string t) {
    if(s.size() == 0)return 1;
      if(s.size() > t.size())return 0;
      int i = 0;
      int j = 0;
      while(j < t.size()){
        if(s[i] == t[j]){
            i++;
            j++;
            if(i >= s.size())return 1;
        }else j++;
      }

      return 0;
  }

